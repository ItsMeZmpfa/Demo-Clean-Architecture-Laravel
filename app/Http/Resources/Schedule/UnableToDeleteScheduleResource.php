<?php

    namespace App\Http\Resources\Schedule;

    use Illuminate\Http\JsonResponse;
    use Illuminate\Http\Request;
    use Illuminate\Http\Resources\Json\JsonResource;

    class UnableToDeleteScheduleResource extends JsonResource
    {

        public function __construct()
        {

        }

        public function withResponse(Request $request, JsonResponse $response)
        {
            parent::withResponse($request, $response->setStatusCode(403)); // TODO: Change the autogenerated stub
        }

        public function toResponse($request)
        {
            return parent::toResponse($request)->status(400);
        }

        /**
         * Transform the resource into an array.
         *
         * @param  Request  $request
         * @return array<string, mixed>
         */
        public function toArray($request)
        {

            return [
                'message' => "Unable to Delete Schedule.",
            ];
        }
    }
